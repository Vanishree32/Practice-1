
public class PrimitiveTypeCasting {

	public static void main(String[] args) {
	
		//Data Widening(lower to higher data type)
		
		int a=10;
		long b=a;
		System.out.println(b);
		
		//Data Narrowing(Higher to lower datatype)
		double c =5.4;
		float d=(float)c;//explicitly
		System.out.println(d);
		
		
		
		

	}

}
